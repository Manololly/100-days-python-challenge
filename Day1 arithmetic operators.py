#getting values of a and b from user
a = int(input("Enter the value of a : "))
b = int(input("Enter the value of b : "))

#Addition
c = a + b
print(f" The addition of a and b is {c}")

#subtraction
c = a - b
print(f" The subtraction of a and b is {c}")

#Multiplication
c = a * b
print(f" The multiplication of a and b is {c}")

#Division
c = a / b
print(f" The division of a and b is {c}")

#Modulus
c = a % b
print(f" The remainder value of a/b is {c}")

#Exponentiation
c = a ** b
print(f" The exponent value of a and b is {c}")

#Floor Division(rounds the nearest value)
c = a // b
print(f" The Floor division of a and b is {c}")